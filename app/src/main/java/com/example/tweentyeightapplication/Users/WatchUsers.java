package com.example.tweentyeightapplication.Users;

public class WatchUsers {
    private int image;
    private String addStoryText;

    public WatchUsers(int image, String addStoryText) {
        this.image = image;
        this.addStoryText = addStoryText;
    }

    public int getImage() {
        return image;
    }

    public String getAddStoryText() {
        return addStoryText;
    }
}
